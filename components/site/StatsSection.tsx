"use client";

import { useEffect, useRef, useState } from "react";

const stats = [
  { label: "Yardım Kampanyası", value: 150, suffix: "+", icon: "heart" },
  { label: "Ulaşılan Kişi", value: 50000, suffix: "+", icon: "users" },
  { label: "Yıllık Deneyim", value: 15, suffix: " Yıl", icon: "calendar" },
  { label: "Gönüllü Bağışçı", value: 3500, suffix: "+", icon: "hand" },
];

function AnimatedNumber({ value, suffix }: { value: number; suffix: string }) {
  const [count, setCount] = useState(0);
  const ref = useRef<HTMLSpanElement>(null);
  const hasAnimated = useRef(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting && !hasAnimated.current) {
          hasAnimated.current = true;
          const duration = 2000;
          const steps = 60;
          const stepValue = value / steps;
          let current = 0;
          const timer = setInterval(() => {
            current += stepValue;
            if (current >= value) {
              setCount(value);
              clearInterval(timer);
            } else {
              setCount(Math.floor(current));
            }
          }, duration / steps);
        }
      },
      { threshold: 0.3 }
    );

    if (ref.current) observer.observe(ref.current);
    return () => observer.disconnect();
  }, [value]);

  const formatted =
    count >= 1000
      ? new Intl.NumberFormat("tr-TR").format(count)
      : count.toString();

  return (
    <span ref={ref} className="text-4xl md:text-5xl font-bold text-primary">
      {formatted}
      {suffix}
    </span>
  );
}

