export function useAdminNav() {
  const [navGroups, setNavGroups] = useState<NavGroup[]>(FALLBACK_NAV);
  const [loading, setLoading] = useState(true);

  const fetchNav = async () => {
    try {
      const res = await fetch("/api/admin/modules/nav", { cache: "no-store" });
      const json = await res.json();
      if (json.success && json.data) {
        setNavGroups(json.data);
      }
    } catch {
      // Fallback zaten set
    }
    setLoading(false);
  };

  useEffect(() => {
    fetchNav();
  }, []);

  // Modul degisikliklerini dinle ve sidebar'i yenile
  useEffect(() => {
    const handleModulesChanged = () => {
      fetchNav();
    };
    window.addEventListener("modules-changed", handleModulesChanged);
    return () => {
      window.removeEventListener("modules-changed", handleModulesChanged);
    };
  }, []);

  return { navGroups, loading, refetch: fetchNav };
}
